package CS101.MidTermPractice.P1;

/* Write a program that inputs two double numbers a and b and calculate their sum, subtraction and multiplication. */
public class Main {

    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
    }
}